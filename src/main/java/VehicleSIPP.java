public class VehicleSIPP{
    String name;
    String sipp;
    String type;
    String doors;
    String transmission;
    String fuel;
    String airCon;

    VehicleSIPP(String name, String sipp, String type, String doors, String transmission, String fuel, String airCon){
        this.name = name;
        this.sipp = sipp;
        this.type = type;
        this.doors = doors;
        this.transmission = transmission;
        this.fuel = fuel;
        this.airCon = airCon;
    }
}